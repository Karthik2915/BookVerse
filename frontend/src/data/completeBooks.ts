// Helper function to get complete books by genre
export function getCompleteBooksByGenre(genre: string) {
  if (genre === "All") return completeBooks;
  return completeBooks.filter(book => book.genre === genre);
}

// Helper function to get complete books by language
export function getCompleteBooksByLanguage(language: string) {
  if (language === "All") return completeBooks;
  return completeBooks.filter(book => book.language === language);
}

// Helper function to get a random complete book
export function getRandomCompleteBook() {
  return completeBooks[Math.floor(Math.random() * completeBooks.length)];
}

